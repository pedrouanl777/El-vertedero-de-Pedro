# Pues solamente defino lo que necesito por lo equivalente a 1 km,C°, L :)
def km_a_millas(km):
    return km * 0.621371

def celsius_a_fahrenheit(celsius):
    return (celsius * 9/5) + 32

def litros_a_galones(litros):
    return litros * 0.264172

